package br.com.srpavani.srpavani.math

import kotlin.math.sqrt


class SimpleMath {
    fun sum(numberOne: Double, number2: Double)= numberOne + number2;
    fun sub(numberOne: Double, number2: Double)= numberOne - number2;
    fun multi(numberOne: Double, number2: Double)= numberOne * number2;
    fun div(numberOne: Double, number2: Double)= numberOne / number2;
    fun avg(numberOne: Double, number2: Double)= (numberOne + number2)/2;
    fun sqrtRoot(number: Double)= sqrt(number)
}